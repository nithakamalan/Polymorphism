package Polymorphism;

public class Mark {

	public void Mark(int x) {
		System.out.println("Print the number"+x);
		
	}

	public void Mark(int x, int y) {
		System.out.println("sum of two marks"+(x+y));
		
		
	}

	public void Mark(int x, int y, int z) {
		System.out.println("Average of 3 marks"+((x+y+z)/2));
		
	}
	
	

}
